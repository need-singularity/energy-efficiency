# 1. 크리스탈러 중심지이론: 6각형 시장권의 필연성
# ═══════════════════════════════════════════════════════════════
print("\n━━━ [1/10] 크리스탈러 중심지이론 ━━━")
print("  → 발터 크리스탈러(1933): 2D 평면에서 동일 면적 원형 시장권의 최밀 배치 = 6각형")

# k=3 체계 (시장 원리): 각 상위 도시 주변 6개 하위 도시
판정_정수("k=3 시장원리 — 주변 도시 수", 6, n, "n = 6", "크리스탈러")

# k=4 체계 (교통 원리): 교통로 6방향
판정_정수("k=4 교통원리 — 주요 교통축", 6, n, "n = 6", "크리스탈러")

# k=7 체계 (행정 원리): 중심 + 6 = 7 = σ - sopfr
판정_정수("k=7 행정원리 — 행정구역 수", 7, σ - sopfr, "σ - sopfr = 12 - 5", "크리스탈러")

# 6각형이 원형 대비 면적 효율: 정육각형/외접원 = 3√3/(2π) ≈ 0.827
효율_hex = 3 * math.sqrt(3) / (2 * math.pi)
효율_예측 = (n // φ) * math.sqrt(n // φ) / (φ * math.pi)  # (n/φ)·√(n/φ)/(φπ) = 3√3/(2π)
판정("6각형 면적 효율 (vs 외접원)", 효율_hex, 효율_예측, "(n/φ)·√(n/φ)/(φπ)", "크리스탈러", 1.0)

# 2D에서 원형 충전 가능한 정다각형: 정삼각형(3), 정사각형(4), 정육각형(6) → 가장 큰 n
판정_정수("2D 타일링 최대 정다각형 변 수", 6, n, "n = 6", "크리스탈러")

# 벌집 추측(Hales 2001): 6각형이 단위면적당 둘레 최소
변_hex = math.sqrt(2 / (3 * math.sqrt(3)))  # 면적 1인 정육각형 변 길이
둘레_hex = n * 변_hex  # 6a
둘레_sq = 4.0  # 면적 1인 정사각형 둘레
절감율 = 1 - 둘레_hex / 둘레_sq
판정("벌집 추측 — 둘레 절감율 (vs 정사각형)", 절감율, 1 / (σ + φ), "1/(σ+φ) = 1/14", "크리스탈러", 5)

