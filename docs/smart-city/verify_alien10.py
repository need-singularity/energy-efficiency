# 2. 도시 교통망: 6방향 교차로 용량 최적화
# ═══════════════════════════════════════════════════════════════
print("\n━━━ [2/10] 도시 교통망 — 6방향 교차로 ━━━")
print("  → 4방향 vs 6방향 교차로 교통 용량 시뮬레이션")

# 교차로 용량 모델: 단위시간 처리 차량 = 방향수 × 비충돌 쌍 비율
# n-way 교차로: 동시 비충돌 쌍 = floor(n/2)
# 처리량 ∝ n × floor(n/2) / (n*(n-1)/2) = n × floor(n/2) × 2 / (n(n-1))

def 교통_효율(방향수):
    """방향수별 교통 처리 효율 (비충돌 동시통과율)"""
    비충돌_쌍 = 방향수 // 2
    전체_쌍 = 방향수 * (방향수 - 1) // 2
    return 비충돌_쌍 / 전체_쌍

효율_4 = 교통_효율(4)  # 2/6 = 0.333
효율_6 = 교통_효율(6)  # 3/15 = 0.200

# 실제 처리량 = 방향수 × 동시통과쌍 × 회전율
# 6방향: 6 × 3 = 18 단위, 4방향: 4 × 2 = 8 단위 → 비율 = 18/8 = 2.25
처리량_4 = 4 * (4 // 2)  # 8
처리량_6 = 6 * (6 // 2)  # 18
처리량_비율 = 처리량_6 / 처리량_4
판정("6방향 vs 4방향 처리량 비율", 처리량_비율, n * (n // φ) / (τ * φ), "n·(n/φ)/(τ·φ) = 6·3/8", "교통망", 1)

# 신호 대기 시간: 6방향 = 3위상 (대향 쌍), 4방향 = 2위상
판정_정수("6방향 교차로 신호 위상 수", 3, n // φ, "n/φ = 3", "교통망")
판정_정수("4방향 교차로 신호 위상 수", 2, φ, "φ = 2", "교통망")

# 그린웨이브: 60° 간격 6방향 → 인접 교차로 동기화 최적 (120° 위상차 = 1/3주기)
위상차 = 1 / (n // φ)  # 1/3 주기
판정("그린웨이브 위상차 (주기 비율)", 1/3, 위상차, "1/(n/φ) = 1/3", "교통망")

# 교통 네트워크 평균 연결도: 6각 그리드의 노드 차수 = 3 (3방향 도로)
판정_정수("6각 그리드 노드 차수", 3, n // φ, "n/φ = 3", "교통망")

# 최적 블록 크기: 보행 10분(800m) → 반지름 400m, 6각 면적
반지름_m = 400
면적_hex = 3 * math.sqrt(3) / 2 * 반지름_m**2  # ≈ 415,692 m²
면적_sq = (2 * 반지름_m)**2  # 640,000 m²
면적_비 = 면적_hex / 면적_sq
판정("6각/정사각 블록 면적비", 면적_비, (n // φ) * math.sqrt(n // φ) / φ**(n // φ), "(n/φ)√(n/φ)/φ^(n/φ)", "교통망", 1)

