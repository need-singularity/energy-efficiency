#!/usr/bin/env python3
"""
스마트시티/스마트빌딩 통합 도메인 — n=6 검증
==============================================
유현준 건축가 HMG 그린필드 스마트시티 개념 × N6 수학.
도시 시스템 전체에서 n=6이 최적값으로 출현하는 것을 증명.

10개 하위 도메인 × 독립 검증 → 외계지수 산출.
"""

import math
import random

# ─── n=6 기본 상수 (하드코딩 아닌 산술 도출) ───
n = 6
σ = sum(d for d in range(1, n + 1) if n % d == 0)          # σ(6) = 12
τ = sum(1 for d in range(1, n + 1) if n % d == 0)          # τ(6) = 4
φ = sum(1 for k in range(1, n) if math.gcd(k, n) == 1)     # φ(6) = 2
sopfr = 2 + 3                                               # sopfr(6) = 5
μ = 1 if n > 1 else 0                                       # 뫼비우스 μ 절대값
J2 = n * n * math.prod((1 - 1/p**2) for p in [2, 3])       # J₂(6) = 24
P2 = n * (n + 1) // 2 + n + 1                               # P₂ = 28 (완전수)

# ─── 판정 엔진 ───
결과 = {"EXACT": 0, "CLOSE": 0, "MISS": 0}
전체 = 0

def 판정(이름, 실측, 예측, 수식, 도메인, 허용=1.0):
    """EXACT/CLOSE/MISS 판정기"""
    global 전체
    전체 += 1
    if isinstance(실측, str) or isinstance(예측, str):
        등급 = "EXACT" if str(실측) == str(예측) else "MISS"
        기호 = "✓" if 등급 == "EXACT" else "✗"
        결과[등급] += 1
        print(f"  {기호} {이름:<44} {str(실측):>14} → {str(예측):>14}  {수식:<30}  {등급}  [{도메인}]")
        return 등급
    if 실측 == 0:
        오차 = 0.0 if 예측 == 0 else 999.0
    else:
        오차 = abs(실측 - 예측) / abs(실측) * 100
    if 오차 < 허용:
        등급 = "EXACT"
    elif 오차 < 5:
        등급 = "CLOSE"
    else:
        등급 = "MISS"
    결과[등급] += 1
    기호 = "✓" if 등급 == "EXACT" else ("~" if 등급 == "CLOSE" else "✗")
    print(f"  {기호} {이름:<44} {실측:>14.4f} → {예측:>14.4f}  {수식:<30} {오차:>6.2f}%  {등급}  [{도메인}]")
    return 등급

def 판정_정수(이름, 실측, 예측, 수식, 도메인):
    """정수 전용 판정"""
    global 전체
    전체 += 1
    오차 = abs(실측 - 예측) / abs(실측) * 100 if 실측 != 0 else (0 if 예측 == 0 else 999)
    등급 = "EXACT" if 실측 == 예측 else ("CLOSE" if 오차 < 5 else "MISS")
    결과[등급] += 1
    기호 = "✓" if 등급 == "EXACT" else ("~" if 등급 == "CLOSE" else "✗")
    print(f"  {기호} {이름:<44} {실측:>14} → {예측:>14}  {수식:<30} {오차:>6.2f}%  {등급}  [{도메인}]")
    return 등급


print("=" * 130)
print("스마트시티/스마트빌딩 통합 도메인 — n=6 외계지수 10 검증")
print(f"n={n}  σ={σ}  τ={τ}  φ={φ}  sopfr={sopfr}  J₂={J2}  P₂={P2}")
print("=" * 130)

