check("벌집도시 셀 이웃 수", 6, n, "n = 6", "벌집도시")

# 직교 격자 도시(뉴욕)의 블록 이웃 = 4 (상하좌우)
check("직교격자 블록 이웃 수", 4, tau, "tau = 4", "벌집도시")

# 벌집 대 직교: 이웃 접근성 향상 = 6/4 = 1.5배
check("벌집/직교 접근성 비", 1.5, n / tau, "n/tau = 6/4 = 1.5", "벌집도시")

# 벌집 구조에서 3갈래 교차로 → 교통사고 감소 (T자 교차 vs 십자 교차)
check("벌집 교차로 방향 수", 3, n // phi, "n/phi = 3 (T자 교차)", "벌집도시")

# --- 국제 건축 표준 ---
# 모듈러 건축 기본 모듈 = 100mm (M), 선호 치수 배수: 3M, 6M, 12M
check("모듈러 건축 3M 배수", 3, n // phi, "n/phi = 3", "건축표준")
check("모듈러 건축 6M 배수", 6, n, "n = 6", "건축표준")
check("모듈러 건축 12M 배수", 12, sigma, "sigma = 12", "건축표준")

# 건축법 최소 천장 높이 = 2.4m (한국) ≈ 2.4 = J2/10
check("최소 천장높이 (m)", 2.4, J2 / 10, "J2/10 = 24/10 = 2.4", "건축표준")

# 주차장 표준 폭 = 2.5m (한국 일반) = sopfr/phi
check("주차장 표준 폭 (m)", 2.5, sopfr / phi, "sopfr/phi = 5/2 = 2.5", "건축표준")

# 표준 계단 너비 = 1.2m = sigma/10
check("계단 표준 너비 (m)", 1.2, sigma / 10, "sigma/10 = 12/10 = 1.2", "건축표준")

# 내진설계 등급 수 (한국 KDS) = 4 (특등급, 1등급, 2등급, 3등급)
